package engSoftFatec.br.engSoftFatec.models;

public class Carro {
    public String Nome;
    public String Marca;
    public float Valor;
    public int Ano;

    public Carro(String nome, String marca, float valor, int ano) {
        this.Nome = nome;
        this.Marca = marca;
        this.Valor = valor;
        this.Ano = ano;
    }
}
